package entity;

/**
 *
 * @author TienNp
 */
public class Brand {
    private String id;
    private String country;

    public Brand() {
    }

    public Brand(String id, String country) {
        this.id = id;
        this.country = country;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }
    
}
